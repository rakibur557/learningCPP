/*
    Types of inheritance
  -------------------------
There are 5 types of inheritance in c++

    1. Single: In this sort of inheritance,
    one subclass inherit from one superclass.

    2. Multilevel: In this sort of inheritance,
    the superclass for one is the subclass for another.

    3. Hierarchical: In this sort of inheritance,
    multiple subclass derived from single superclass.

    4. Multiple: In multiple inheritance, a class can inherit more than one class.
    In this sort of inheritance, a single child class can have multiple parent classes.

    5. Hybrid: hybrid inheritance is a combination of more than one type of inheritance.
*/
